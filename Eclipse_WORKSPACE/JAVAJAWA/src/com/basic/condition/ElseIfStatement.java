package com.basic.condition;

public class ElseIfStatement {
	public static void main(String[] args) {
		int x = 15;
		if (x == 20) {
			System.out.println("x == 20");
		}
		else if (x > 20) {
			System.out.println("x > 20");		
			}
		else {
			System.out.println("x < 20");
		}
		System.out.println("the END");
	}
}
